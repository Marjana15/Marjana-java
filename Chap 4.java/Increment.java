public class Increment 
{
    public static void main(String[] args)
    {
        int C = 5;
        System.out.printf("c before postincrement: %d%n", C);
        System.out.printf(" postincrementing c:%d%n", ++C);
        System.out.printf(" c after preincrement:%d%n",  C);

        System.out.println();

        C = 5;
        System.out.printf("c before postincrement: %d%n", C);
        System.out.printf(" postincrementing c:%d%n", ++C);
        System.out.printf(" c after preincrement:%d%n",  C);



    }
}
